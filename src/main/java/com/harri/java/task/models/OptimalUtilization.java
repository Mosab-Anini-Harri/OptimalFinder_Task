package com.harri.java.task.models;

public class OptimalUtilization {
    private String brandName;
    private Integer sourceRepoId;
    private Integer destRepoId;

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public Integer getSourceRepoId() {
        return sourceRepoId;
    }

    public void setSourceRepoId(Integer sourceRepoId) {
        this.sourceRepoId = sourceRepoId;
    }

    public Integer getDestRepoId() {
        return destRepoId;
    }

    public void setDestRepoId(Integer destRepoId) {
        this.destRepoId = destRepoId;
    }
}
